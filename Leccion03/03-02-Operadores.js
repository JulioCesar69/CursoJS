let a = 3, b = 2;
let z = a + b;

//incremento
//pre-incremento (el operador ++ antes de la variable)
z= ++a;
console.log(a);
console.log(z);

//post-incremento (el operador ++ despues de la variable)
z = b++;
console.log(b);
console.log(z);

//Decremento
//predecremento 
z = --a;
console.log(a);
console.log(z);

//postdecremento
z = b--;
console.log(b);
console.log(z);

